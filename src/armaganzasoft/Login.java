    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String login=jTextField1.getText();
        String password=jPasswordField1.getText();
        
        int indice = 0;     
        if(login.isEmpty() || password.isEmpty())   
        mensaje.showMessageDialog(rootPane, "Debes llenar todos los campos", "Error", mensaje.ERROR_MESSAGE);
        else{
             String nombre="";
            for(indice=0; indice<5; indice++){
                                nombre=misClientesRMI.validarUsuarioRemoto(login, password, indice);
                if(!nombre.isEmpty()){                    
                    break;
                }
            }
            if(nombre.isEmpty()){
                mensaje.showMessageDialog(rootPane, "     ¡¡¡Acceso Denegado!!! \n        Intente Nuevamente", "Error", mensaje.ERROR_MESSAGE);
                jTextField1.setText("");
                jPasswordField1.setText("");          
            } else{
                mensaje.showMessageDialog(rootPane, "Bienvenido: " + nombre + "\nIP: " + urlBD[indice] + "", "EXITO",  mensaje.INFORMATION_MESSAGE);
                FrmMenu menu= new FrmMenu();
                menu.setVisible(true);
                dispose();
            }
        }
    }                                        

