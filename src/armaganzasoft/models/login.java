    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String login=jTextField1.getText();
        String password=jPasswordField1.getText();
        if(login.isEmpty() || password.isEmpty())
            mensaje.showMessageDialog(rootPane, "Debes llenar todos los campos!!!", "Error", mensaje.ERROR_MESSAGE);
        else{
            String nombre="";
            nombre=misUsurios.validarUsario(login,password);
            if(!nombre.isEmpty());
           
                if(nombre.isEmpty()){
                mensaje.showMessageDialog(rootPane, "Nombre del usuario o contraseña incorrectos", "Error", mensaje.ERROR_MESSAGE);
                jTextField1.setText("");
                jPasswordField1.setText("");
            } else{
                mensaje.showMessageDialog(rootPane, "Bienvenido "+nombre, "Acceso Correcto", mensaje.INFORMATION_MESSAGE);
                FrmPrincipal principal= new FrmPrincipal();
                principal.setVisible(true);
                dispose();
            }
        }
    }                                        

